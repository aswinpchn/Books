# A tuple is an immutable list. This means that once you assign objects to a tuple,
# the tuple cannot be changed under any circumstance.
# It is often useful to think of a tuple as a constant list.

# Most new Python programmers scratch their head in bemusement when they
# first encounter tuples, as it can be hard to work out their purpose. After all,
# what use is a list that cannot change? It turns out that there are plenty of use
# cases where you’ll want to ensure that your objects can’t be changed by your (or
# anyone else’s) code. We’ll return to tuples in the next chapter (as well as later in
# this book) when we talk about them in a bit more detail, as well as use them.