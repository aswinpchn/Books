print(7 / 7)  # In Python 3, "/" uniformly works as a float division operator.

print(5 // 2)  # This is floor division

print(5 < 2 and 5 > 2)  # 'and', 'or' is used in python.

print([1, 2, 3] == [1, 2, 3])  # We can quickly check if two lists are same with this.
print([1, 2, 3] == [1, 2, 4])  # False

print({1, 2, 3} == {3, 2, 1})  # True

print(5 not in [1, 2, 3])  # True
