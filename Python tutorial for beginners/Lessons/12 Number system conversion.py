# Python provides rich support in number system conversions

a = bin(25)
print(a, type(a))  # 0b11001 <class 'str'>

b = 0b11001  # If a number is written with '0b' on the front it becomes a integer.
print(b, type(b))  # 25 <class 'int'>

# The above concept is somewhat similar for hex and octa also.
